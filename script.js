        const itemsPerPage = 3;
        let currentPage = 0;
        
        function displayPlaylist() {
            const container = document.getElementById('playlist-container');
            const start = currentPage * itemsPerPage;
            const end = start + itemsPerPage;
            const pageItems = playlist.slice(start, end);
            
            container.innerHTML = pageItems.map((song, index) => `
                <a href="#" class="list-group-item list-group-item-action ${index === 0 && currentPage === 0 ? 'active' : ''}">
                    <div class="d-flex justify-content-between align-items-center">
                        <div>${song.title}</div>
                        <small>${song.duration}</small>
                    </div>
                </a>
            `).join('');
            
            updatePaginationButtons();
        }
        
        function updatePaginationButtons() {
            const prevBtn = document.getElementById('prevPage');
            const nextBtn = document.getElementById('nextPage');
            
            prevBtn.disabled = currentPage === 0;
            nextBtn.disabled = (currentPage + 1) * itemsPerPage >= playlist.length;
        }
        
        document.getElementById('prevPage').addEventListener('click', () => {
            if (currentPage > 0) {
                currentPage--;
                displayPlaylist();
            }
        });
        
        document.getElementById('nextPage').addEventListener('click', () => {
            if ((currentPage + 1) * itemsPerPage < playlist.length) {
                currentPage++;
                displayPlaylist();
            }
        });
        
        // Initial display
        displayPlaylist();

        let currentTrack = 0;
        const audio = document.getElementById("birthday-song");
        const playBtn = document.getElementById("playBtn");
        const prevBtn = document.getElementById("prevBtn");
        const nextBtn = document.getElementById("nextBtn");
        const nowPlaying = document.getElementById("nowPlaying");
        const progress = document.getElementById("progress");
        
        function togglePlay() {
            if (audio.paused) {
                audio.play();
                playBtn.innerHTML = '<i class="fas fa-pause"></i>';
            } else {
                audio.pause();
                playBtn.innerHTML = '<i class="fas fa-play"></i>';
            }
        }
        
        function playNext() {
            currentTrack = (currentTrack + 1) % playlist.length;
            loadTrack();
        }
        
        function playPrev() {
            currentTrack = (currentTrack - 1 + playlist.length) % playlist.length;
            loadTrack();
        }
        
        function loadTrack() {
            audio.src = playlist[currentTrack].src;
            nowPlaying.textContent = `Now Playing: ${playlist[currentTrack].title}`;
            updateActiveTrack();
            audio.play();
            playBtn.innerHTML = '<i class="fas fa-pause"></i>';
        }
        
        function updateActiveTrack() {
            const tracks = document.querySelectorAll('.list-group-item');
            tracks.forEach((track, index) => {
                if (index === currentTrack) {
                    track.classList.add('active');
                } else {
                    track.classList.remove('active');
                }
            });
        }

        let currentSlideIndex = 0;
        const slides = document.querySelectorAll('.slide');

        function updateSlides() {
            slides.forEach((slide, index) => {
                slide.classList.remove('active', 'left', 'right');

                if (index === currentSlideIndex) {
                    slide.classList.add('active');
                } else if (index === (currentSlideIndex - 1 + slides.length) % slides.length) {
                    slide.classList.add('left');
                } else if (index === (currentSlideIndex + 1) % slides.length) {
                    slide.classList.add('right');
                }
            });
        }

        function startSlideshow() {
            setInterval(() => {
                currentSlideIndex = (currentSlideIndex + 1) % slides.length;
                updateSlides();
            }, 2500); // Change slide every 1 second
        }

        // Initialize the slideshow
        updateSlides();
        startSlideshow();

        
        audio.addEventListener('timeupdate', () => {
            const percentage = (audio.currentTime / audio.duration) * 100;
            progress.style.width = percentage + '%';
        });
        
        playBtn.addEventListener('click', togglePlay);
        nextBtn.addEventListener('click', playNext);
        prevBtn.addEventListener('click', playPrev);
        
        // Add click events to playlist items
        document.querySelectorAll('.list-group-item').forEach((item, index) => {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                currentTrack = index;
                loadTrack();
            });
        });

        window.addEventListener('load', function() {
            // Trigger click on the play button to start playing when the page loads
            playBtn.addEventListener('click', togglePlay);
        });

